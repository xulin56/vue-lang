<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <h2>Essential Links</h2>
    <ul>
      <li>
        <a
          href="https://vuejs.org"
          target="_blank"
        >
          Core Docs
        </a>
      </li>
      <li>{{$t('message.hello')}}</li>
      <li><button @click="changeLang(1)">英文</button></li>
      <li><button @click="changeLang(2)">日文</button></li>
    </ul>
  </div>
</template>

<script>
export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App'
    }
  },
  methods: {
    changeLang(lang) {
        if(lang == 1){
            this.$i18n.locale = 'en';
        }else if(lang ==2) {
            this.$i18n.locale = 'jp'
        }
    }
  }
}
</script>

